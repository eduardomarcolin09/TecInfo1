/*      Estruturas Condicionais

    Uma estrutura condicional é utilizada quando um comando ou 
    um bloco de comandos dependem de determinada condição para ser executado.

    Essa condicação DEVE ser sempre uma EXPRESSÃO LÓGICA
    com operadores relacionais e/ou lógicos.

    =-=-= Sintaxe =-=-=

    if(condição){
        // comandos
    }

    * Adicionais *

    -> Verificar se o número é par:

    let n = 5
    if(n % 2 === 0){
        console.log('é par')
    }

    -> Verificar se o número é ímpar:
    let n = 5
    if(n % 2 === 1){
        console.log('é ímpar')
    }
    
 */